#include "luacallback.h"
#include "modules/wgt.h"
#include <luaz/lua.h>

void luaCallback( lua_State *L, int ref, bool value ) {
  if (ref != LUA_NOREF) {
    lua_rawgeti(L, LUA_REGISTRYINDEX, ref);
    lua_pushboolean(L, value);
    lua_call(L, 1, 0);
  }
}

void luaCallback( lua_State *L, int ref) {
  if (ref != LUA_NOREF) {
    lua_rawgeti(L, LUA_REGISTRYINDEX, ref);
    lua_call(L, 0, 0);
  }
}
